    @Override
    public void gerar()
{
    //tipos embalagens
    String sql = "SELECT nome FROM tipo_embalagem";

    try (Connection conn = ConexaoDB.conectar();
         Statement stmt = conn.createStatement();
         ResultSet resultado = stmt.executeQuery(sql)) 
    {
 while (resultado.next())
            {
                //aqui adiciona o resultado encontrado(nome do tipo) a lista,usando getString (que transforma o resultado para uma List<String> nesse caso
                TIPO_EMBALAGEM_CB.addItem(resultado.getString("nome"));
            } 
    }
    catch (SQLException e) 
            {
        JOptionPane.showMessageDialog(null, "Erro ao conectar com o banco de dados: " + e.getMessage());
            }
    //produtos associados
     String sqlProdutos = "SELECT * FROM produtos";

    try 
    {
        Connection conn = ConexaoDB.conectar();
        Statement stmt = conn.createStatement();
        ResultSet resultado = stmt.executeQuery(sqlProdutos); 
   
        while (resultado.next()) 
        {
            Construtor_Produtos produto_associado = new Construtor_Produtos
            (
                
                resultado.getString("nome"),
                resultado.getString("descricao"),
                resultado.getString("familia"),
                resultado.getString("tipo"),
                resultado.getString("lote"),
                resultado.getString("observacoes"),
                new Dimensoes
                (
                resultado.getDouble("altura"),
                resultado.getDouble("largura"),
                resultado.getDouble("comprimento")
                ),
                resultado.getDouble("peso")
            );
            
            produtos.add(produto_associado); // mantém a referência
            PRODUTO_ASSOCIADO_CB.addItem(produto_associado.getNome()); // só mostra o nome
        } 
    }
    catch (SQLException e) 
            {
        JOptionPane.showMessageDialog(null, "Erro ao conectar com o banco de dados: " + e.getMessage());
            }
}

    private void BOTAOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BOTAOActionPerformed
        
        // Validação
        if (OB_TEXT_AREA.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Valor Inserido é Invalido!");
        }
        
        if (Double.parseDouble(ALTURA_TXT.getText().trim()) <= 0 || Double.parseDouble(LARGURA_TXT.getText().trim()) <= 0
            || Double.parseDouble(COMPRIMENTO_TXT.getText().trim()) <= 0 || Double.parseDouble(PESO_TXT.getText().trim()) <= 0){
            JOptionPane.showMessageDialog(null, "Valor Inserido é Negativo ou Zero!");
        }
        
        Construtor_Produtos produto_selecionado = produtos.get(PRODUTO_ASSOCIADO_CB.getSelectedIndex());
        if(produto_selecionado == null) 
        {
            JOptionPane.showMessageDialog(null, "Selecione um produto válido!");
            return;
        }
        
       boolean empilhavel;
       if( SIM_OP.isSelected() )
        {
           empilhavel=true; 
        }
        else
        {
           empilhavel=false; 
        }
        
        boolean sucesso = DaoEmbalagem.salvar
(      
        new Construtor_Embalagens
    (
       produto_selecionado,
       new Dimensoes
        (
        Double.parseDouble(ALTURA_TXT.getText().trim()),
        Double.parseDouble(LARGURA_TXT.getText().trim()),
        Double.parseDouble(COMPRIMENTO_TXT.getText().trim())
        ),
        Double.parseDouble(PESO_TXT.getText().trim()),
        empilhavel,
        OB_TEXT_AREA.getText()
         
    ),
        produto_selecionado.getId(),
        TIPO_EMBALAGEM_CB.getSelectedIndex() + 1 
);  
        if(sucesso == true)
        {
            JOptionPane.showMessageDialog(null, "cadastrado com suceso!");
        }else
        {
            JOptionPane.showMessageDialog(null, "inválido!");
        }
    }//GEN-LAST:event_BOTAOActionPerformed


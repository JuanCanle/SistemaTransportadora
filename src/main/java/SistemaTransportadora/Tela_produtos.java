 @Override
    public void gerar()
    {
        String sql = "SELECT nome FROM grau_fragilidade";

    try (Connection conn = ConexaoDB.conectar();
         Statement stmt = conn.createStatement();
         ResultSet resultado = stmt.executeQuery(sql)) 
    {
 while (resultado.next())
            {
                //aqui adiciona o resultado encontrado(nome do tipo) a lista,usando getString (que transforma o resultado para uma List<String> nesse caso
                GRAU_FRAGILIDADE_CB.addItem(resultado.getString("nome"));
            } 
    }
    catch (SQLException e) 
            {
        JOptionPane.showMessageDialog(null, "Erro ao conectar com o banco de dados: " + e.getMessage());
            }
    }
    private void BOTAOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BOTAOActionPerformed
       
        // Validação
        if (NOME_TXT.getText().trim().length() <  3 || DESCRICAO_TXT.getText().trim().isEmpty()
            || FAMILIA_TXT.getText().trim().isEmpty() || TIPO_TXT.getText().trim().isEmpty()
            || LOTE_TXT.getText().trim().isEmpty() || OB_TEXT_AREA.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Valor Inserido é Invalido!");
        }
        
        if (Double.parseDouble(ALTURA_TXT.getText().trim()) <= 0 || Double.parseDouble(LARGURA_TXT.getText().trim()) <= 0
            || Double.parseDouble(COMPRIMENTO_TXT.getText().trim()) <= 0 || Double.parseDouble(PESO_TXT.getText().trim()) <= 0){
            JOptionPane.showMessageDialog(null, "Valor Inserido é Negativo ou Zero!");
        }
        
        boolean sucesso = DaoProduto.salvar
(      
        new Construtor_Produtos
    (
        NOME_TXT.getText().trim(),
        DESCRICAO_TXT.getText().trim(),
        FAMILIA_TXT.getText().trim(),
        TIPO_TXT.getText().trim(),
        LOTE_TXT.getText().trim(),
        OB_TEXT_AREA.getText().trim(),
       new Dimensoes
        (
        Double.parseDouble(ALTURA_TXT.getText().trim()),
        Double.parseDouble(LARGURA_TXT.getText().trim()),
        Double.parseDouble(COMPRIMENTO_TXT.getText().trim())
        ),
        Double.parseDouble(PESO_TXT.getText().trim())
    ),
        GRAU_FRAGILIDADE_CB.getSelectedIndex() + 1 // Supondo que o ID começa em 1
);        
       if(sucesso == true)
        {
            JOptionPane.showMessageDialog(null, "cadastrado com suceso!");
        }else
        {
            JOptionPane.showMessageDialog(null, "inválido!");
        }
    }//GEN-LAST:event_BOTAOActionPerformed


